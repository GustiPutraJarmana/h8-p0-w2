// 1. Melakukan Looping Menggunakan While
console.log("LOOPING PERTAMA");
var pertama = 0;

while (pertama < 19) {
  pertama = pertama + 2;
  console.log(pertama + " - I love coding");
}
console.log("LOOPING KEDUA");
var kedua = 22;

while (kedua > 2) {
  kedua = kedua - 2;
  console.log(kedua + " - I will become fullstack developer");
}
